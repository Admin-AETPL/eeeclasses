 <p align="justify">At the national level 3000  students are selected for the final test from entire states and UT. Every state  has a proportionate quota of students for the final test. The quota for the  Uttar Pradesh is approximately 550. A stipulated number of candidates  who qualified at the national level examination were called for face-to-face  interview. The award of scholarships was finally determined on the basis of the  candidates' scores obtained in all the three components namely the MAT, the SAT  and the Interview. </p>
 <p align="justify"> The amount of  scholarship is Rs 500/- per month for all the students studying in Class  IX onwards (irrespective of the class/course)  except for Ph.D., wherein it is paid as per UGC norms. </p>
 
