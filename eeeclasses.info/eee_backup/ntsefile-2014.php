  <td colspan="2"><p class="style10">Details  of National Talent Search Exam</p>
    <p align="justify">The NTSE is conducted by the  NCERT. The first level of examination ( known as STSE) is conducted by the  authorities of the state government, which do follow almost the same pattern as  laid down by the NCERT. But the states had complete autonomy to design  and conduct their written examinations. However, they were advised to follow  the national pattern which comprised MAT and SAT.</p>
    <ul type="square">
      <li> The   MAT (Mental ability Test) and SAT (Scholastic  Aptitude Test )  will consist  of 90 questions each.</li>
      <li> SAT will have 35 questions for Social  Science (History, Geography, Civics and Economics), 35 for Science  (Physics, Chemistry, Biology )  and 20 for Mathematics. </li>
      <li> Quota for a state will be computed  proportionally on the basis of student enrolment in Classes VII and VIII. </li>
    </ul>
