 <p class="style10">How to Apply for NTSE</p>
 <ol start="1" type="1">
   <li>1<sup>st</sup> stage is at state level, which is normally held in November. It is conducted at the state level. In some states, application form (July-August)can be downloaded from the website and in some states, you have to collect from the designated centres in the city.</li>
   <li>Submit/Send the completely filled application form as per the instructions on the form. Normally it should be attested by the principal of your school and send by the school authorities to the concerned state</li>
   <li>If you qualifies 1st round, then you are eligible for the 2nd round (national level) , the exam will be conducted in May next year.</li>
 </ol>
